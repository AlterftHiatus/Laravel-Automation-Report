            Angkut. Disebutkan di dalamnya pada pasal 140 ayat 1 bahwa setiap pemasangan dan/atau perakitan, pemakaian,
            atau pengoperasian, pemeliharaan dan perawatan, perbaikan, perubahan atau modifikasi, serta pemeriksaan dan
